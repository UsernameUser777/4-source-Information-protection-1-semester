# Программа для шифрования фамилии методом шифра Цезаря
# Используется русский алфавит

# Исходная фамилия
surname = "Колосов"

# Устанавливаемый сдвиг (ключ шифра Цезаря)
# Сдвиг может быть любым целым числом
# Для примера возьмем сдвиг 3 (классический сдвиг Цезаря)
shift = 3

# Определяем русский алфавит
# Включает все буквы от 'А' до 'Я', включая 'Ё'
alphabet = "АБВГДЕЁЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯ"


# Функция для шифрования строки с помощью шифра Цезаря
def caesar_cipher(text, shift_value, alphabet):
    """
    Шифрует текст с помощью шифра Цезаря.

    :param text: Исходный текст для шифрования (строка).
    :param shift_value: Величина сдвига (целое число).
    :param alphabet: Алфавит, используемый для шифрования (строка).
    :return: Зашифрованный текст (строка).
    """
    # Преобразуем входной текст в верхний регистр
    # Это гарантирует, что мы работаем только с заглавными буквами
    text_upper = text.upper()

    # Инициализируем строку для хранения результата
    encrypted_text = ""

    # Проходим по каждой букве в тексте
    for char in text_upper:
        # Проверяем, находится ли символ в алфавите
        if char in alphabet:
            # Находим индекс символа в алфавите
            old_index = alphabet.index(char)

            # Вычисляем новый индекс с учетом сдвига
            # Используем операцию % (остаток от деления), чтобы обеспечить цикличность
            # Например, при сдвиге 3, 'Я' (последняя буква) станет 'В'
            new_index = (old_index + shift_value) % len(alphabet)

            # Находим букву в алфавите по новому индексу
            new_char = alphabet[new_index]

            # Добавляем новую букву к результату
            encrypted_text += new_char
        else:
            # Если символ не является буквой алфавита (например, пробел или знак препинания),
            # оставляем его без изменений
            encrypted_text += char

    # Возвращаем зашифрованный текст
    return encrypted_text


# Вызываем функцию шифрования с нашими параметрами
encrypted_surname = caesar_cipher(surname, shift, alphabet)

# Выводим результаты
print(f"Исходное сообщение: {surname}")
print(f"Ключ (величина сдвига): {shift}")
print(f"Алфавит: {alphabet}")
print(f"Зашифрованное сообщение: {encrypted_surname}")
