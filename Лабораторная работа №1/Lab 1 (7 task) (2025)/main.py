# Программа для шифрования фамилии методом шифра Виженера
# Используется русский алфавит

# Исходная фамилия
surname = "Колосов"

# Ключевое слово (лозунг) для шифра Виженера
# Ключ будет повторяться для соответствия длине сообщения
keyword = "ДЯДИНА"

# Определяем русский алфавит
# Включает все буквы от 'А' до 'Я', включая 'Ё'
# Для шифрования по модулю N (мощность алфавита) нужно сопоставить буквам числа
alphabet = "АБВГДЕЁЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯ"
# Мощность алфавита N
N = len(alphabet)


# Функция для шифрования строки с помощью шифра Виженера
def vigenere_cipher(text, key, alphabet):
    """
    Шифрует текст с помощью шифра Виженера.

    :param text: Исходный текст для шифрования (строка).
    :param key: Ключевое слово (строка).
    :param alphabet: Алфавит, используемый для шифрования (строка).
    :return: Зашифрованный текст (строка).
    """
    # Преобразуем входной текст и ключ в верхний регистр
    text_upper = text.upper()
    key_upper = key.upper()

    # Инициализируем строку для хранения результата
    encrypted_text = ""

    # Проходим по каждой букве в тексте
    for i, char in enumerate(text_upper):
        # Проверяем, находится ли символ в алфавите
        if char in alphabet:
            # Находим индекс символа в алфавите (его числовое значение P_i)
            plaintext_char_index = alphabet.index(char)

            # Находим индекс символа ключа, соответствующего текущей букве текста
            # Ключ повторяется, поэтому используем остаток от деления на длину ключа
            key_char_index = alphabet.index(key_upper[i % len(key_upper)])

            # Вычисляем индекс зашифрованного символа по формуле Виженера: C_i = (P_i + K_i) mod N
            encrypted_char_index = (plaintext_char_index + key_char_index) % N

            # Находим букву в алфавите по вычисленному индексу
            encrypted_char = alphabet[encrypted_char_index]

            # Добавляем зашифрованную букву к результату
            encrypted_text += encrypted_char
        else:
            # Если символ не является буквой алфавита (например, пробел или знак препинания),
            # оставляем его без изменений
            # В задании сказано шифровать фамилию, так что других символов быть не должно
            # Но на всякий случай добавим обработку
            encrypted_text += char

    # Возвращаем зашифрованный текст
    return encrypted_text


# Вызываем функцию шифрования с нашими параметрами
encrypted_surname = vigenere_cipher(surname, keyword, alphabet)

# Выводим результаты
print(f"Исходное сообщение: {surname}")
print(f"Ключ: {keyword}")
print(f"Алфавит: {alphabet}")
print(f"Мощность алфавита (N): {N}")
print(f"Зашифрованное сообщение: {encrypted_surname}")
